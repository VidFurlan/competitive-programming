ull xorTo(ull x) {
    if (x % 4 == 0) return x;
    if (x % 4 == 1) return 1;
    if (x % 4 == 2) return x + 1;
    return 0;
}

ull xorRange(ull l, ull r) {
    return xorTo(r) ^ xorTo(l - 1);
}

void solve() {
    ull l, r, i, k; 
    cin >> l >> r >> i >> k;

    ull high = xorRange((l - k + (1 << i) - 1) >> i, (r - k) >> i) << i;
    ull low = k * (((r - k) / (1 << i) - (l - k - 1) / (1 << i)) & 1);
    cout << (xorRange(l, r) ^ low ^ high) << endl;
}

